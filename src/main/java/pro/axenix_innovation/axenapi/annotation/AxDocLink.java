package pro.axenix_innovation.axenapi.annotation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Annotation to specify a relative path to a documentation file.
 * When processed, Swagger will add an x-documentation-file-links extension
 * containing the path specified in this annotation.
 */
@Target(ElementType.METHOD)
@Retention(RetentionPolicy.RUNTIME)
public @interface AxDocLink {
    /**
     * Relative path to the documentation file
     */
    String value();
}
